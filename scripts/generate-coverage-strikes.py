def generate_by_node(atoms):
    node_atoms = defaultdict(list)
    for atom in atoms:
        nodes = atom.get("nodes", [])
        if isinstance(nodes, list):
            for n in nodes:
                node_atoms[n].append(atom)
        elif isinstance(nodes, int):
            node_atoms[nodes].append(atom)

    lines = [
        "# Coverage Map: By Node",
        "",
        f"Generated: {timestamp()}",
        f"Total atoms in registry: {len(atoms)}",
        "",
        "---",
        "",
    ]

    for node_id in sorted(NODE_NAMES.keys()):
        node_name = NODE_NAMES[node_id]
        group = node_atoms.get(node_id, [])
        total = len(group)
        exists = sum(1 for a in group if a.get("build_state") == "EXISTS")
        partial = sum(1 for a in group if a.get("build_state") == "PARTIAL")
        missing = sum(1 for a in group if a.get("build_state") == "MISSING")
        na = sum(1 for a in group if a.get("build_state") == "N/A")

        lines.append(f"## Node {node_id} — {node_name}")
        lines.append(
            f"Total atoms: {total} | EXISTS: {exists} | PARTIAL: {partial} | MISSING: {missing} | N/A: {na}"
        )
        lines.append("")

        if group:
            lines.append(
                "| ID | Source | Idea | Provenance | Nature | Build State |"
            )
            lines.append(
                "|-----|--------|------|------------|--------|-------------|"
            )
            # Sort by build priority (MISSING first), then ID
            sorted_group = sorted(
                group,
                key=lambda a: (
                    BUILD_PRIORITY.get(a.get("build_state", "N/A"), 99),
                    a.get("id", ""),
                ),
            )
            for a in sorted_group:
                lines.append(
                    f"| {a.get('id', '')} "
                    f"| {esc(a.get('source_file', ''))} "
                    f"| {truncate(a.get('idea', ''))} "
                    f"| {a.get('provenance', '')} "
                    f"| {a.get('nature', '')} "
                    f"| {a.get('build_state', '')} |"
                )
        else:
            lines.append("*No atoms mapped to this node.*")

        lines.append("")
        lines.append("---")
        lines.append("")

    return "\n".join(lines)


