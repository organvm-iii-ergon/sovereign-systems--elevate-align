def generate_phase_5(atoms):
    """Proof & Close"""
    total = len(atoms)
    exists = sum(1 for a in atoms if a.get("build_state") == "EXISTS")
    partial = sum(1 for a in atoms if a.get("build_state") == "PARTIAL")
    missing = sum(1 for a in atoms if a.get("build_state") == "MISSING")
    na = sum(1 for a in atoms if a.get("build_state") == "N/A")

    # Coverage by phase
    phase_counts = defaultdict(lambda: {"total": 0, "EXISTS": 0, "PARTIAL": 0, "MISSING": 0, "N/A": 0})
    for a in atoms:
        sp = a.get("strike_phase", "unassigned")
        phase_prefix = sp[0] if sp and sp[0].isdigit() else "unassigned"
        phase_counts[phase_prefix]["total"] += 1
        bs = a.get("build_state", "N/A")
        if bs in phase_counts[phase_prefix]:
            phase_counts[phase_prefix][bs] += 1

    # Provenance coverage
    prov_counts = defaultdict(lambda: {"total": 0, "EXISTS": 0, "MISSING": 0})
    for a in atoms:
        prov = a.get("provenance", "UNKNOWN")
        prov_counts[prov]["total"] += 1
        bs = a.get("build_state", "N/A")
        if bs == "EXISTS":
            prov_counts[prov]["EXISTS"] += 1
        elif bs == "MISSING":
            prov_counts[prov]["MISSING"] += 1

    lines = [
        "# Strike Plan: Phase 5 — Proof & Close",
        "",
        f"Generated: {timestamp()}",
        "",
        "Phase 5 is the verification pass. No new content is created —",
        "this phase confirms that all atoms have been accounted for",
        "and the build is complete.",
        "",
        "---",
        "",
        "## Coverage Verification Summary",
        "",
        "### Overall",
        "",
        "| Metric | Count | Pct |",
        "|--------|-------|-----|",
        f"| Total atoms | {total} | 100% |",
        f"| EXISTS | {exists} | {exists*100//total}% |",
        f"| PARTIAL | {partial} | {partial*100//total}% |",
        f"| MISSING | {missing} | {missing*100//total}% |",
        f"| N/A | {na} | {na*100//total}% |",
        "",
        "### By Phase",
        "",
        "| Phase | Total | EXISTS | PARTIAL | MISSING | N/A |",
        "|-------|-------|--------|---------|---------|-----|",
    ]

    for phase in ["1", "2", "3", "4"]:
        pc = phase_counts.get(phase, {"total": 0, "EXISTS": 0, "PARTIAL": 0, "MISSING": 0, "N/A": 0})
        lines.append(
            f"| Phase {phase} | {pc['total']} | {pc['EXISTS']} | {pc['PARTIAL']} | {pc['MISSING']} | {pc['N/A']} |"
        )
    pc_u = phase_counts.get("unassigned", {"total": 0, "EXISTS": 0, "PARTIAL": 0, "MISSING": 0, "N/A": 0})
    if pc_u["total"] > 0:
        lines.append(
            f"| Unassigned | {pc_u['total']} | {pc_u['EXISTS']} | {pc_u['PARTIAL']} | {pc_u['MISSING']} | {pc_u['N/A']} |"
        )

    lines.extend([
        "",
        "### By Provenance",
        "",
        "| Provenance | Total | EXISTS | MISSING | Coverage |",
        "|------------|-------|--------|---------|----------|",
    ])
    for prov in PROVENANCE_ORDER:
        pc = prov_counts.get(prov, {"total": 0, "EXISTS": 0, "MISSING": 0})
        t = pc["total"]
        e = pc["EXISTS"]
        m = pc["MISSING"]
        pct = f"{e*100//t}%" if t > 0 else "0%"
        lines.append(f"| {prov} | {t} | {e} | {m} | {pct} |")

    lines.extend([
        "",
        "### By Node Coverage",
        "",
        "| Node | Name | Total | EXISTS | MISSING |",
        "|------|------|-------|--------|---------|",
    ])

    node_counts = defaultdict(lambda: {"total": 0, "EXISTS": 0, "MISSING": 0})
    for a in atoms:
        nodes = a.get("nodes", [])
        if isinstance(nodes, list):
            for n in nodes:
                node_counts[n]["total"] += 1
                bs = a.get("build_state", "N/A")
                if bs == "EXISTS":
                    node_counts[n]["EXISTS"] += 1
                elif bs == "MISSING":
                    node_counts[n]["MISSING"] += 1
        elif isinstance(nodes, int):
            node_counts[nodes]["total"] += 1
            bs = a.get("build_state", "N/A")
            if bs == "EXISTS":
                node_counts[nodes]["EXISTS"] += 1
            elif bs == "MISSING":
                node_counts[nodes]["MISSING"] += 1

    for nid in sorted(NODE_NAMES.keys()):
        nc = node_counts.get(nid, {"total": 0, "EXISTS": 0, "MISSING": 0})
        lines.append(
            f"| {nid} | {NODE_NAMES[nid]} | {nc['total']} | {nc['EXISTS']} | {nc['MISSING']} |"
        )

    lines.extend([
        "",
        "---",
        "",
        "## Verification Checklist",
        "",
        "- [ ] All MISSING atoms reviewed — confirm intentional omission or create build tickets",
        "- [ ] All FLAGGED atoms resolved (editorial decisions made)",
        "- [ ] LOCAL provenance atoms preserved with Maddie's authentic voice",
        "- [ ] Node coverage balanced across all 13 spiral nodes",
        "- [ ] Pillar coverage balanced across all 4 pillars",
        "- [ ] Cross-cutting atoms integrated into hub architecture",
        "- [ ] proof.md regenerated and showing PROOF COMPLETE",
        "",
        "## Proof Link",
        "",
        "See [proof.md](../coverage/proof.md) for the canonical proof status.",
        "",
        f"Current status: **{total} atoms registered, {exists + partial} in build ({(exists+partial)*100//total}%).**",
    ])

    return "\n".join(lines)


