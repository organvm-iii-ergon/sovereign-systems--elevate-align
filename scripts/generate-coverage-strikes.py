# Build priority: MISSING > PARTIAL > EXISTS > N/A
BUILD_PRIORITY = {"MISSING": 0, "PARTIAL": 1, "EXISTS": 2, "N/A": 3}


def load_registry():
    """Parse the atom registry line-by-line to handle malformed YAML values.

    The registry is generated by awk and may contain unescaped quotes or
    special characters that break strict YAML parsing. This parser reads
    the known fixed-schema format directly.
    """
    atoms = []
    current = None

    with open(REGISTRY_PATH, "r") as f:
        for line in f:
            stripped = line.rstrip("\n")

            # New atom entry
            if stripped.startswith("  - id: "):
                if current is not None:
                    atoms.append(current)
                current = {"id": stripped[len("  - id: "):].strip()}
                continue

            if current is None:
                continue

            # Key-value fields (4-space indent)
            if stripped.startswith("    ") and ": " in stripped:
                key_val = stripped[4:]
                colon_pos = key_val.index(": ")
                key = key_val[:colon_pos]
                val = key_val[colon_pos + 2:]

                # Strip surrounding quotes
                if val.startswith('"') and val.endswith('"'):
                    val = val[1:-1]
                elif val.startswith('"'):
                    # Malformed quote — just strip leading quote
                    val = val[1:]

                # Parse typed values
                if key == "nodes":
                    # Parse [1, 2, 3] or []
                    val = val.strip()
                    if val == "[]":
                        val = []
                    elif val.startswith("[") and val.endswith("]"):
                        inner = val[1:-1]
                        val = [int(x.strip()) for x in inner.split(",") if x.strip()]
                    else:
                        val = []
                elif key in ("position", "of_total"):
                    try:
                        val = int(val)
                    except (ValueError, TypeError):
                        val = 0

                current[key] = val

    # Don't forget the last atom
    if current is not None:
        atoms.append(current)

    return atoms


def timestamp():
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def esc(text):
    """Escape pipe characters for markdown tables."""
    if not text:
        return ""
    return str(text).replace("|", "\\|")


def truncate(text, maxlen=80):
    """Truncate idea text for table display."""
    text = esc(text)
    if len(text) > maxlen:
        return text[: maxlen - 3] + "..."
    return text


