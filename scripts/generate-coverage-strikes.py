def generate_by_pillar(atoms):
    pillar_atoms = defaultdict(list)
    for atom in atoms:
        pillar = atom.get("pillar", "Unknown")
        pillar_atoms[pillar].append(atom)

    lines = [
        "# Coverage Map: By Pillar",
        "",
        f"Generated: {timestamp()}",
        f"Total atoms in registry: {len(atoms)}",
        "",
        "## Summary",
        "",
        "| Pillar | Total | EXISTS | PARTIAL | MISSING | N/A |",
        "|--------|-------|--------|---------|---------|-----|",
    ]

    for pillar in PILLAR_ORDER:
        group = pillar_atoms.get(pillar, [])
        total = len(group)
        exists = sum(1 for a in group if a.get("build_state") == "EXISTS")
        partial = sum(1 for a in group if a.get("build_state") == "PARTIAL")
        missing = sum(1 for a in group if a.get("build_state") == "MISSING")
        na = sum(1 for a in group if a.get("build_state") == "N/A")
        lines.append(
            f"| {pillar} | {total} | {exists} | {partial} | {missing} | {na} |"
        )

    # Catch any pillars not in the canonical list
    other_pillars = sorted(set(pillar_atoms.keys()) - set(PILLAR_ORDER))
    for pillar in other_pillars:
        group = pillar_atoms[pillar]
        total = len(group)
        exists = sum(1 for a in group if a.get("build_state") == "EXISTS")
        partial = sum(1 for a in group if a.get("build_state") == "PARTIAL")
        missing = sum(1 for a in group if a.get("build_state") == "MISSING")
        na = sum(1 for a in group if a.get("build_state") == "N/A")
        lines.append(
            f"| {pillar} | {total} | {exists} | {partial} | {missing} | {na} |"
        )

    lines.append("")
    lines.append("---")
    lines.append("")

    # Detail per pillar
    all_pillars = PILLAR_ORDER + other_pillars
    for pillar in all_pillars:
        group = pillar_atoms.get(pillar, [])
        if not group:
            continue

        # Nature distribution
        nature_counts = defaultdict(int)
        for a in group:
            nature_counts[a.get("nature", "UNKNOWN")] += 1

        lines.append(f"## {pillar}")
        lines.append(f"Total: {len(group)}")
        lines.append("")
        lines.append("### Nature Distribution")
        lines.append("")
        lines.append("| Nature | Count |")
        lines.append("|--------|-------|")
        for nature, count in sorted(
            nature_counts.items(), key=lambda x: -x[1]
        ):
            lines.append(f"| {nature} | {count} |")

        lines.append("")
        lines.append("### Atoms")
        lines.append("")
        lines.append(
            "| ID | Source | Idea | Nature | Build State |"
        )
        lines.append(
            "|-----|--------|------|--------|-------------|"
        )
        sorted_group = sorted(
            group,
            key=lambda a: (
                BUILD_PRIORITY.get(a.get("build_state", "N/A"), 99),
                a.get("id", ""),
            ),
        )
        for a in sorted_group:
            lines.append(
                f"| {a.get('id', '')} "
                f"| {esc(a.get('source_file', ''))} "
                f"| {truncate(a.get('idea', ''))} "
                f"| {a.get('nature', '')} "
                f"| {a.get('build_state', '')} |"
            )
        lines.append("")
        lines.append("---")
        lines.append("")

    return "\n".join(lines)


