def atom_row(a):
    return (
        f"| {a.get('id', '')} "
        f"| {esc(a.get('source_file', ''))} "
        f"| {truncate(a.get('idea', ''))} "
        f"| {a.get('provenance', '')} "
        f"| {a.get('nature', '')} "
        f"| {a.get('build_state', '')} |"
    )


def strike_table_header():
    return (
        "| ID | Source | Idea | Provenance | Nature | Build State |\n"
        "|-----|--------|------|------------|--------|-------------|"
    )


def sort_atoms(group):
    return sorted(
        group,
        key=lambda a: (
            BUILD_PRIORITY.get(a.get("build_state", "N/A"), 99),
            a.get("id", ""),
        ),
    )


def generate_phase_1(atoms):
    """Foundation (UNBLOCKED)"""
    # Strike 1A: Architecture atoms (nature: FRAMEWORK, MODEL, INSTRUCTION from root docs)
    strike_1a = [
        a
        for a in atoms
        if a.get("strike_phase", "").startswith("1A")
        and a.get("nature") in ("FRAMEWORK", "MODEL", "INSTRUCTION")
    ]

    # Strike 1B: Editorial triage (editorial: FLAGGED)
    strike_1b = [a for a in atoms if a.get("editorial") == "FLAGGED"]

    # Strike 1C: Client decision extraction (nature: QUESTION, INSTRUCTION)
    strike_1c = [
        a
        for a in atoms
        if a.get("strike_phase", "").startswith("1")
        and a.get("nature") in ("QUESTION", "INSTRUCTION")
    ]

    lines = [
        "# Strike Plan: Phase 1 — Foundation (UNBLOCKED)",
        "",
        f"Generated: {timestamp()}",
        "",
        "Phase 1 establishes the architectural and editorial foundation.",
        "No external blockers — all work can begin immediately.",
        "",
        "---",
        "",
        f"## Strike 1A: Architecture Atoms ({len(strike_1a)})",
        "",
        "Atoms with nature FRAMEWORK, MODEL, or INSTRUCTION from foundational source documents.",
        "These define the structural skeleton of the spiral system.",
        "",
        strike_table_header(),
    ]
    for a in sort_atoms(strike_1a):
        lines.append(atom_row(a))

    lines.extend([
        "",
        "---",
        "",
        f"## Strike 1B: Editorial Triage ({len(strike_1b)})",
        "",
        "All atoms flagged for editorial review. These require human decision",
        "before they can advance to any build state.",
        "",
        strike_table_header(),
    ])
    for a in sort_atoms(strike_1b):
        lines.append(atom_row(a))

    lines.extend([
        "",
        "---",
        "",
        f"## Strike 1C: Client Decision Extraction ({len(strike_1c)})",
        "",
        "QUESTION and INSTRUCTION atoms from Phase 1 — decisions Maddie needs to make",
        "or directives that need to be confirmed before build proceeds.",
        "",
        strike_table_header(),
    ])
    for a in sort_atoms(strike_1c):
        lines.append(atom_row(a))

    total = len(set(a["id"] for a in strike_1a + strike_1b + strike_1c))
    lines.extend([
        "",
        "---",
        "",
        f"## Phase 1 Summary",
        "",
        f"| Strike | Count |",
        f"|--------|-------|",
        f"| 1A — Architecture | {len(strike_1a)} |",
        f"| 1B — Editorial Triage | {len(strike_1b)} |",
        f"| 1C — Client Decisions | {len(strike_1c)} |",
        f"| **Unique atoms** | **{total}** |",
    ])

    return "\n".join(lines)


